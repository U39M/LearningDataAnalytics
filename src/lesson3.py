import pandas as pd

df = pd.read_excel("../data/pokemon_data.xlsx", sheet_name = 'Pokemon_1')

# 特定の列を取り出す
## 名前のカラムを取り出す
print('#################################################')
print(df['名前'])
print(df.名前)
print('#################################################')

## 名前とIDのカラムを取り出す
print('#################################################')
print(df[['名前', 'id']])
print('#################################################')

# 指定した範囲のデータを取り出す
## 上から100件取り出す
print('#################################################')
print(df.head(100))
print(df.iloc[:100,:])
print('#################################################')

## 上から201から205までのデータを取りだす
print('#################################################')
print(df.iloc[200:205,:])
print('#################################################')

## 上から201から205までの行で、名前とタイプ1・2を取り出す
print('#################################################')
print(df.iloc[200:205,1:4])
print('#################################################')

# 条件を付けてデータを取り出す
print('#################################################')
print(df.query('タイプ1=="みず"'))
print(df[df['タイプ1']=='みず'])
print('#################################################')

# 複数の条件を付けてデータを取り出す
## AND
print('#################################################')
print(df.query('タイプ1=="みず"&タイプ2=="こおり"'))
print('#################################################')

## OR
print('#################################################')
print(df.query('タイプ1=="みず"|タイプ2=="みず"'))
print('#################################################')

## 数値の条件
print('#################################################')
print(df.query('高さ<=1'))
print('#################################################')